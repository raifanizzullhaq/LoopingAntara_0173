#include <iostream>
using namespace std;

int main() {
    //perulangan dengan for
    cout << "PERULANGAN FOR" << endl;

    //perulangan dengan for naik
    for (int i = 0; i < 4; i++) {
        cout << "Nilai i : " << i << " Teknologi Informasi UMY" << endl;
    }
    cout << endl;
    //perulangan dengan for turun
    for (int i = 6; i > 0; i--) {
        cout << "Nilai i : " << i << " Teknologi Informasi UMY" << endl;

    }
    cout << endl;

    return 0;
}